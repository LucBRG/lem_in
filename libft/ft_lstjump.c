#include "libft.h"

t_list	*ft_lstjump(t_list **lst, size_t n)
{
	size_t	i;

	i = -1;
	while (++i < n)
	{
		if (!(*lst = (*lst)->next))
			return (NULL);
	}
	return (*lst);
}
